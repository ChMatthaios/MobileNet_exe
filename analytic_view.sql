SELECT 
    C.CUSTOMER_NAME AS NAME,
    C.CUSTOMER_AFM AS AFM,
    CT.CTYPE_NAME AS TYPE,
    S.SUBSCRIPTION_NUMBER AS TELEPHONE,
    SPR.SUBS_PL_START_DATE AS START_DATE,
    SP.SPLAN_NAME AS PLAN,
    SP.SPLAN_FIX AS FIX,
    B.BILL_AMOUNT AS PRICE,
    B.BILL_REM AS BALANCE
FROM CUSTOMER C
	JOIN CUSTOMER_TYPE CT
		ON C.CUSTOMER_TID = CT.CTYPE_ID
	JOIN SUBSCRIPTION S
		ON C.CUSTOMER_ID = S.SUBSCRIPTION_CID
	JOIN SUBS_PL_RELATION SPR
		ON S.SUBSCRIPTION_ID = SPR.SUBS_ID
	JOIN SUBSCRIPTION_PLAN SP
		ON SPR.SUBS_PL_ID = SP.SPLAN_ID
	JOIN BILLING B
		ON C.CUSTOMER_ID = B.BILL_CID
			AND S.SUBSCRIPTION_ID = B.BILL_SID
GROUP BY
    C.CUSTOMER_NAME,
    C.CUSTOMER_AFM,
    CT.CTYPE_NAME,
    S.SUBSCRIPTION_NUMBER,
    SPR.SUBS_PL_START_DATE,
    SP.SPLAN_NAME,
    SP.SPLAN_FIX,
    B.BILL_AMOUNT,
    B.BILL_REM
ORDER BY
    S.SUBSCRIPTION_NUMBER;

